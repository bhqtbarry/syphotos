function t(string $key): string
{
    $locale = current_locale();
    $bundle = translations();
    if (!isset($bundle[$locale])) {
        $locale = 'en';
    }
    return $bundle[$locale][$key] ?? $bundle['en'][$key] ?? $key;
}
